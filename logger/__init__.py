    def info(self,txt):
        time = self._time
        if self._colored:print(Fore.LIGHTGREEN_EX+Style.BRIGHT+self._format.format(h=time.hour,m=time.minute,s=time.second,root=self.root,level="INFO",content=txt)+Style.RESET_ALL,file=self._stream.file)
