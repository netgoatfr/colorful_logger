            if self._colored:print(Fore.RED+Style.BRIGHT+"["+self._time+"]"+" ["+self.root+"] [EXCEPTION]: "+line+Style.RESET_ALL,file=self.file)
            else:print("["+self._time+"]"+" ["+self.root+"] [EXCEPTION]: "+line,file=self._stream.file)
